//type casing in c++=used to convert int value into a float value and float value convert into a int value type casing are used
#include<iostream>
using namespace std;
int main()
{
    float a=12.56;
    int c=14;
    cout<<"the value of a is "<<(int)a<<endl;
    cout<<"the value of c is "<<(float)c;
    return 0;
}